class circle:

    def __init__(self,r):
        self.radius = r


    def area(self):
        return 3.14 * self.radius * self.radius



    def perimeter(self):
        return 2 * self.radius * 3.14

cir = circle(25)
print(circle(25).area())
print(circle(25).perimeter())




















